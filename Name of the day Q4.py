Day = int(input("Enter a day in (1-7): "))
if Day==1:
    print("The day",Day,"is Sunday.")
elif Day==2:
    print("The day",Day,"is Monday.")
elif Day==3:
    print("The day",Day,"is Tuesday.")
elif Day==4:
    print("The day",Day,"is Wednesday.")
elif Day==5:
    print("The day",Day,"is Thursday.")
elif Day==6:
    print("The day",Day,"is Friday.")
elif Day==7:
    print("The day",Day,"is Saturday.")
else:
    print("Invalid day, Please enter the number between (1-7).")

    